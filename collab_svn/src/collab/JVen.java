	@Override
	public String toString()
	{
		String ans = null;
		
		switch ( (int)(3*Math.random()) )
		{
			
			case 0:
				ans = "<Justin Venezuela>";
				break;
				
			case 1:
				ans = "<Agustin Venezuela>";
				break;
				
			case 2:
				ans = "<jven>";
				break;
				
			default:
				ans = "Math is broken.";
				
		}
		
		return ans;
	}
	
