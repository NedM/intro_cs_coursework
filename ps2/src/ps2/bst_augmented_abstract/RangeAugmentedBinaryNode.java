package ps2.bst_augmented_abstract;

/**
 * An augmented Binary Node stores a parent pointer, and an integer value for
 * each node.
 */
public class RangeAugmentedBinaryNode extends AugmentedBinaryNode {
	int augmentedValue;
	protected int minimumValueInSubtree;
	protected int maximumValueInSubtree;
	
	BinaryNode parent;

	// Constructor
	RangeAugmentedBinaryNode(int key, int augmentedValue, BinaryNode parent) {
		super(key, augmentedValue, parent);
	}
}