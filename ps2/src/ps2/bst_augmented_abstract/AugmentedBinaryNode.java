package ps2.bst_augmented_abstract;

/**
 * An augmented Binary Node stores a parent pointer, and an integer value for
 * each node.
 */
public class AugmentedBinaryNode extends BinaryNode {
	int augmentedValue;
	BinaryNode parent;

	// Constructor
	AugmentedBinaryNode(int key, int augmentedValue, BinaryNode parent) {
		super(key);
		this.augmentedValue = augmentedValue;
		this.parent = parent;
	}
}