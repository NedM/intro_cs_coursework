package postscript.commands;

/**
 * Represents the postscript command `n setlinewidth'.
 */
public class Setlinewidth implements PostscriptCommand {
	
	private double n;

	public Setlinewidth(double n) {
		this.n = n;
	}
	
	public String toString() {
		return Double.toString(n) + " setlinewidth\n";
	}
}
